package com.example.jpdeguzman.popularmovies.moviesearch;

/**
 * Used by the menu to sort by different movie types
 */
public enum MovieSearchType {
    /**
     * Movies that are sorted by 'popular'
     */
    MOVIE_TYPE_POPULAR,

    /**
     * Movies that are sorted by 'top_rated'
     */
    MOVIE_TYPE_TOP_RATED,

    /**
     * Movies that are sorted by 'favorite'
     */
    MOVIE_TYPE_FAVORITE
}
